import { Fragment, useCallback } from "react";
import { Menu, Transition } from "@headlessui/react";
import { Link, useNavigate } from "react-router-dom";
import { useAuthContext } from "../../hooks/useAuthContext";
import * as ActionType from "../../context/constants";
import { signinTokenInitial } from "../model";

export const Dropdown = () => {
  const navigate = useNavigate();
  const { signinToken, dispatchAuth } = useAuthContext();

  const handleSignOut = useCallback(async () => {
    await fetch(`${import.meta.env.VITE_API_BASE_URL_LOCAL}auth/signout`, {
      method: "GET",
      headers: {
        Authorization: `Bearer ${signinToken.accessToken}`,
      },
    });

    dispatchAuth({
      type: ActionType.ACT_USER_LOGIN,
      payload: signinTokenInitial,
    });
    localStorage.clear();
    sessionStorage.clear();
    return navigate("/sign-in");
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [signinToken.accessToken, navigate]);

  return (
    <div className="text-start">
      <Menu as="div" className="relative inline-block text-left">
        <div className="hover:border-b-2 hover:border-slate-400 h-7 duration-100">
          <Menu.Button className="font-semibold text-slate-600 hover:text-slate-500">
            Menu
          </Menu.Button>
        </div>
        <Transition
          as={Fragment}
          enter="transition ease-out duration-100"
          enterFrom="transform opacity-0 scale-95"
          enterTo="transform opacity-100 scale-100"
          leave="transition ease-in duration-75"
          leaveFrom="transform opacity-100 scale-100"
          leaveTo="transform opacity-0 scale-95"
        >
          <Menu.Items className="absolute left-0 mt-2 w-56 origin-top-right divide-y divide-gray-100 rounded-md bg-white shadow-lg ring-1 ring-black ring-opacity-5 focus:outline-none">
            <div className="px-1 py-1 ">
              <Link to="/mentor">
                <Menu.Item>
                  {({ active }) => (
                    <button
                      className={`${
                        active ? "bg-violet-500 text-white" : "text-gray-900"
                      } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                    >
                      {active ? (
                        <DuplicateActiveIcon />
                      ) : (
                        <DuplicateInactiveIcon />
                      )}
                      Mentors
                    </button>
                  )}
                </Menu.Item>
              </Link>
              <Link to="/classroom">
                <Menu.Item>
                  {({ active }) => (
                    <button
                      className={`${
                        active ? "bg-violet-500 text-white" : "text-gray-900"
                      } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                    >
                      {active ? (
                        <DuplicateActiveIcon />
                      ) : (
                        <DuplicateInactiveIcon />
                      )}
                      Classrooms
                    </button>
                  )}
                </Menu.Item>
              </Link>
              <Link to="/student">
                <Menu.Item>
                  {({ active }) => (
                    <button
                      className={`${
                        active ? "bg-violet-500 text-white" : "text-gray-900"
                      } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                    >
                      {active ? (
                        <DuplicateActiveIcon />
                      ) : (
                        <DuplicateInactiveIcon />
                      )}
                      Students
                    </button>
                  )}
                </Menu.Item>
              </Link>
            </div>
            <div className="px-1 py-1">
              <Menu.Item>
                {({ active }) => (
                  <button
                    className={`${
                      active ? "bg-violet-500 text-white" : "text-gray-900"
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                  >
                    {active ? <EditActiveIcon /> : <EditInactiveIcon />}
                    Profile
                  </button>
                )}
              </Menu.Item>
              <Menu.Item>
                {({ active }) => (
                  <button
                    className={`${
                      active ? "bg-violet-500 text-white" : "text-gray-900"
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                  >
                    {active ? <MoveActiveIcon /> : <MoveInactiveIcon />}
                    Settings
                  </button>
                )}
              </Menu.Item>
            </div>
            <div className="px-1 py-1">
              <Menu.Item>
                {({ active }) => (
                  <button
                    className={`${
                      active ? "bg-violet-500 text-white" : "text-gray-900"
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                    onClick={handleSignOut}
                  >
                    {active ? <DeleteActiveIcon /> : <DeleteInactiveIcon />}
                    Sign out
                  </button>
                )}
              </Menu.Item>
            </div>
          </Menu.Items>
        </Transition>
      </Menu>
    </div>
  );
};

